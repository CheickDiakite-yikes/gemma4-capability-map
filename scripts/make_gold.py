ROOT = Path(__file__).resolve().parents[1]
GOLD_DIR = ROOT / "data" / "gold"
ASSET_DIR = ROOT / "data" / "assets"
REGISTRY = build_default_registry()
SPECS = {spec.name: spec for spec in REGISTRY.list_specs()}


def main() -> None:
    GOLD_DIR.mkdir(parents=True, exist_ok=True)
    thinking_tasks = build_thinking_tasks()
    tool_tasks = build_tool_routing_tasks()
    retrieval_tasks = build_retrieval_tasks()
    agent_tasks = build_full_stack_tasks()
    dump_jsonl(GOLD_DIR / "thinking.jsonl", thinking_tasks)
    dump_jsonl(GOLD_DIR / "tools.jsonl", tool_tasks)
    dump_jsonl(GOLD_DIR / "retrieval.jsonl", retrieval_tasks)
    dump_jsonl(GOLD_DIR / "agents.jsonl", agent_tasks)
    print(f"Wrote {sum(len(batch) for batch in [thinking_tasks, tool_tasks, retrieval_tasks, agent_tasks])} seed tasks.")


